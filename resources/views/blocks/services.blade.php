          <p>RESEARCH PHILOSOPHY <br> <br>
          
[ Multidisciplinary and Integrative ] <br>


Our research philosophy revolves around multidisciplinary and integrative approaches. Designers should comprehend knowledge from various domains and effectively lead the team to drive design-led innovation. Therefore we work with various experts from diverse fields.
<br> <br>
We employ systematic human-centered methods to achieve this objective, allowing us to generate innovative design concepts. These concepts are then implemented and validated using ergonomics and engineering design techniques in products and services. 
Furthermore, we explore entrepreneurial avenues to commercialize our designs successfully.
<br> <br>
[ Grounded in Pragmatism ] <br> Our research is firmly grounded in pragmatism. We begin our research with design projects rooted in practical requirements, ensuring our outcomes are always geared towards practical contributions. As a result, 
